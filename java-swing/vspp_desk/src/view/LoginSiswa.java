    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         Connection con = config.koneksi.connect();
         String Get_nisn = nisn.getText();
         String Get_nama = nama.getText();
        
        try {
            Statement st = con.createStatement();
            String sql = "SELECT * FROM siswa WHERE nisn='"+nisn.getText()+"' and nama = '"+nama.getText()+"'";
            ResultSet rs = st.executeQuery(sql);
            
            if (rs.next()) {
                String nisn = rs.getString("nisn");
                String nama = rs.getString("nama");
                
                UserSession.set_nisn(nisn);
                UserSession.set_nama(nama);
                
                if (rs.getString("level").equals("SISWA")) {
                    Siswa ow = new Siswa();
                    ow.show();
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password and Username Incorrect");
                nisn.setText("");
                nama.setText("");
                
            }
        }catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

